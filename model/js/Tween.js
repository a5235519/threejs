/*
 * Tween  （demo地址: http://www.zhangxinxu.com/study/201612/how-to-use-tween-js.html ）
 * start: 开始时间  （默认值即可）
 * begin: 起始值 
 * end: 结束值  （创造补间动画）
 * during: 持续时间
 * delay: 延迟时间
*/

function Tween(object){
    var $this = this;
    $this.reset = $.extend({
        start: 0,
        begin: 0,  //起始值
        end: 0,   //结束值
        during: 0,  //持续时间
        delay: 0,   //延迟时间
        easing: 'Linear',  //动画曲线
        callback: function(){},
        endcallback: function(){}
    },object);
    $this.maxNum= 0;
    $this.val= 0;
    $this.easingArr = [];
    $this.easingFun = function(){};

    $this.maxNum = parseInt($this.reset.during/16);
    $this.easingArr = $this.reset.easing.split('.');
    $this.easingFun = $this.setOptions();

    if($this.reset.delay == 0 && !$this.reset.delay){
        $this.animation();
    }else{
        setTimeout(function(){
            $this.animation();
        },$this.reset.delay);
    }
}

Tween.prototype.setOptions = function(){
    var $this = this;
    if ($this.easingArr.length == 1) {
        fnGetValue = easings[$this.easingArr[0]];
        return fnGetValue;
    } else if ($this.easingArr.length == 2) {
        fnGetValue = easings[$this.easingArr[0]] && easings[$this.easingArr[0]][$this.easingArr[1]];
        return fnGetValue;
    }
}

Tween.prototype.animation = function(){
    var $this = this;
    $this.val = $this.easingFun($this.reset.start, $this.reset.begin, $this.reset.end, $this.maxNum)
    $this.reset.start++;
    if ($this.reset.start <= $this.maxNum) {
        $this.reset.callback($this.val);
        requestAnimationFrame(function(){
            $this.animation($this)
        });
    } else {
        $this.reset.endcallback();
    }
}


